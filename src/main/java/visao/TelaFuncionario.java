    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        Funcionario funcionarioSel = lstFuncionarios.getSelectedValue();
        if (funcionarioSel != null) {
            System.out.println("Funcionário: " + funcionarioSel.getMatricula());
            try {
                jpa.conexaoAberta();

                int delOp = JOptionPane.showConfirmDialog(this,
                        "Tem certeza que deseja remover " + funcionarioSel.getNome() + "?");
                if (delOp == JOptionPane.YES_OPTION) {
                    jpa.remover(funcionarioSel);
                }

                jpa.fecharConexao();
                carregarFuncionariosCadastrados();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,
                        "Erro ao remover funcionário " + funcionarioSel + "\n" + e);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um funcionário para remover");
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

    private void cmbDepartamentoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbDepartamentoItemStateChanged
        Departamento departamentoSel = (Departamento) cmbDepartamento.getSelectedItem();
        if (departamentoSel == null) {
            carregarFuncionariosCadastrados();
        } else {
            jpa.conexaoAberta();

            DefaultListModel modeloLista = new DefaultListModel();
            modeloLista.addAll(jpa.getFuncionarios(departamentoSel));
            lstFuncionarios.setModel(modeloLista);

            jpa.fecharConexao();
        }
    }//GEN-LAST:event_cmbDepartamentoItemStateChanged

    private void txtBuscaFuncionarioKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscaFuncionarioKeyReleased
        if(txtBuscaFuncionario.getText().trim().isEmpty()){
            carregarFuncionariosCadastrados();
        } else{
            jpa.conexaoAberta();
            DefaultListModel modeloLista = new DefaultListModel();
            modeloLista.addAll(jpa.getFuncionarios(txtBuscaFuncionario.getText().trim()));
            System.out.println("Funcinários carregados: "+jpa.getFuncionarios(txtBuscaFuncionario.getText().trim()));
            lstFuncionarios.setModel(modeloLista);

            jpa.fecharConexao();
        }
    }//GEN-LAST:event_txtBuscaFuncionarioKeyReleased

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        Funcionario funcionarioSel = lstFuncionarios.getSelectedValue();
        if (funcionarioSel != null) {
            TelaCadastroFuncionario telaEdt = new TelaCadastroFuncionario(this, rootPaneCheckingEnabled);
            telaEdt.setFuncionario(funcionarioSel);
            telaEdt.setVisible(true);
            
            carregarFuncionariosCadastrados();
            
        } else {
            JOptionPane.showMessageDialog(this, "Selecione um funcionário para Editar");
        }
    }//GEN-LAST:event_btnEditarActionPerformed


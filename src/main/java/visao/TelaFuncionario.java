    public void carregarFuncionariosCadastrados() {
        jpa.conexaoAberta();

        DefaultListModel modeloLista = new DefaultListModel();
        modeloLista.addAll(jpa.getFuncionarios());
        lstFuncionarios.setModel(modeloLista);

        jpa.fecharConexao();

    }

    public void carregarDepartamentos() {
        cmbDepartamento.removeAllItems();
        cmbDepartamento.addItem(null);
        for (Departamento item : Departamento.values()) {
            cmbDepartamento.addItem(item);
        }
    }


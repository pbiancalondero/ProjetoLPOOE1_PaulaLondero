    public void carregarTipos(){
        cmbTipo.removeAllItems();
        for(TipoFuncionario item: TipoFuncionario.values()){
            cmbTipo.addItem(item);
        }
    }
    
    public void carregarDepartamentos(){
        cmbDepartamento.removeAllItems();
        for(Departamento item: Departamento.values()){
            cmbDepartamento.addItem(item);
        }
    }
    


    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        if(funcionario == null){
            funcionario = new Funcionario();
        }
        
        funcionario.setNome(txtNome.getText());
        funcionario.setMatricula(txtID.getText());
        funcionario.setCpf(txtCpf.getText());
        funcionario.setSenha(txtSenha.getText());
        funcionario.setTipo((TipoFuncionario)cmbTipo.getSelectedItem());
        funcionario.setDepartamento((Departamento)cmbDepartamento.getSelectedItem());
        
        jpa.conexaoAberta();
        try {
            jpa.persist(funcionario);
            System.out.println("Funcionário cadastrado com sucesso");
        } catch (Exception ex) {
            Logger.getLogger(TelaFuncionario.class.getName()).log(Level.SEVERE, null, ex);
            System.out.println("Erro ao persistir funcionário: "+funcionario+" \n Erro: "+ex);
        }
        jpa.fecharConexao();
        dispose();
    }//GEN-LAST:event_btnSalvarActionPerformed

